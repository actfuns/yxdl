package com.wanniu.game.data.base;


public class FourProp {
    public String prop;
    public int par;
    public int min;
    public int max;

    public FourProp() {
    }

    public FourProp(String prop, int par, int min, int max) {
        this.prop = prop;
        this.par = par;
        this.min = min;
        this.max = max;
    }
}


