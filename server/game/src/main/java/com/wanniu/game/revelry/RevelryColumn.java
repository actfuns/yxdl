package com.wanniu.game.revelry;


public class RevelryColumn {
    private String id;
    private String name;
    private String label;
    private String goto1;
    private String goto2;
    private String tip;

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLabel() {
        return this.label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getGoto1() {
        return this.goto1;
    }

    public void setGoto1(String goto1) {
        this.goto1 = goto1;
    }

    public String getGoto2() {
        return this.goto2;
    }

    public void setGoto2(String goto2) {
        this.goto2 = goto2;
    }

    public String getTip() {
        return this.tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }
}


